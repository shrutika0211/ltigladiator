package lti.model.com;

public class Employee 
{
    String EmpName;
    int EmpId;
    
    
	public String getEmpName() {
		return EmpName;
	}
	
	
	public void setEmpName(String empName) {
		EmpName = empName;
	}
	public int getEmpId() {
		return EmpId;
	}
	public void setEmpId(int empId) {
		EmpId = empId;
	}
}
